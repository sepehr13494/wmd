      Country(
        name: "SA",
        countryName: AppLocalizations.of(context).countries_SA,
      ),
      Country(
        name: "AE",
        countryName: AppLocalizations.of(context).countries_AE,
      ),
    ];

    final List<Country> others = List.from(
      json(context).entries.map(
        (e) {
          return Country(name: e.key, countryName: e.value);
        },
      ),
    );
    others.sort((a, b) => a.countryName.compareTo(b.countryName));
    important.addAll(others);
    return important;
